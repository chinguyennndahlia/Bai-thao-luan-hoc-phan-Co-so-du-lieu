SELECT *
FROM NHAN_VIEN
WHERE DiaChi = N'Hòa Bình'

SELECT *
FROM BANG_LUONG
WHERE HeSoLuong > 4
ORDER BY LuongCoBan ASC

SELECT *
FROM BANG_LUONG
WHERE TongLuong BETWEEN '8000000' AND '10000000'

SELECT *
FROM NHAN_VIEN
WHERE MaCV = 'TP'
ORDER BY HeSoLuong DESC

SELECT COUNT(*) as SoLuongNVNu
FROM NHAN_VIEN
WHERE GioiTinhNV = N'Nữ' AND MaPB='NS01'

SELECT DISTINCT NV.TenNV 
FROM NHAN_VIEN NV
JOIN THUONG_KHAUTRU TK ON NV.MaNV = TK.MaNV
WHERE TK.Thuong > 0 AND TK.KhauTru > 0

SELECT AVG(BL.TongLuong) AS TrungBinhLuongPhongKeToan
FROM BANG_LUONG BL
JOIN NHAN_VIEN NV ON BL.MaNV = NV.MaNV
WHERE NV.MaPB = 'KT03'

SELECT SUM(BL.TongLuong) AS TongLuongPhongMarketing
FROM BANG_LUONG BL
JOIN NHAN_VIEN NV ON BL.MaNV = NV.MaNV
WHERE NV.MaPB = 'MKT02'

SELECT NV.MaPB, PB.TenPB, AVG(BL.TongLuong) AS LuongTrungBinh
FROM NHAN_VIEN NV
JOIN BANG_LUONG BL ON NV.MaNV = BL.MaNV
JOIN PHONG_BAN PB ON NV.MaPB = PB.MaPB
GROUP BY NV.MaPB, PB.TenPB
HAVING AVG(BL.TongLuong) > 10000000

SELECT NV.MaNV, NV.TenNV, NV.MaPB, PB.TenPB, BL.TongLuong
FROM NHAN_VIEN NV
JOIN BANG_LUONG BL ON NV.MaNV = BL.MaNV
JOIN PHONG_BAN PB ON NV.MaPB = PB.MaPB
WHERE NV.MaPB IN (
    SELECT MaPB
    FROM NHAN_VIEN NV2
    JOIN BANG_LUONG BL2 ON NV2.MaNV = BL2.MaNV
    GROUP BY NV2.MaPB
    HAVING AVG(BL2.TongLuong) > 10000000)

SELECT NV.MaNV, NV.TenNV, NV.MaPB, PB.TenPB, BL.TongLuong
FROM NHAN_VIEN NV
JOIN BANG_LUONG BL ON NV.MaNV = BL.MaNV
JOIN PHONG_BAN PB ON NV.MaPB = PB.MaPB
WHERE BL.TongLuong = (
    SELECT MAX(BL2.TongLuong)
    FROM BANG_LUONG BL2
    JOIN NHAN_VIEN NV2 ON NV2.MaNV = BL2.MaNV
    WHERE NV2.MaPB = NV.MaPB)
