CREATE DATABASE QUANLYLUONG
GO

USE QUANLYLUONG
GO

CREATE TABLE  PHONG_BAN (
 MaPB VARCHAR(10) NOT NULL PRIMARY KEY,
 TenPB NVARCHAR(40),
 SoLuongNV INT
 )
 GO

 CREATE TABLE CHUC_VU (
 MaCV VARCHAR(10) NOT NULL PRIMARY KEY,
 TenCV NVARCHAR(40)
 )
 GO

 CREATE TABLE NHAN_VIEN (
 MaNV VARCHAR(10) NOT NULL PRIMARY KEY,
 TenNV NVARCHAR(40), 
 GioiTinhNV NCHAR(3),
 DiaChi NVARCHAR(100),
 SDT CHAR(10),
 MaPB VARCHAR(10),
 MaCV VARCHAR(10),
 HeSoLuong DECIMAL(5,2),
 FOREIGN KEY (MaPB) REFERENCES PHONG_BAN(MaPB),
 FOREIGN KEY (MaCV) REFERENCES CHUC_VU(MaCV)
 )
 GO

 CREATE TABLE CHAM_CONG (
 MaCC VARCHAR(10) NOT NULL PRIMARY KEY,
 MaNV VARCHAR(10) NOT NULL,
 SoNgayCong INT,
 SoGioLam INT,
 FOREIGN KEY (MaNV) REFERENCES NHAN_VIEN(MaNV)
 )
 GO

CREATE TABLE THUONG_KHAUTRU (
 MaGD CHAR(10) NOT NULL PRIMARY KEY,
 MaNV VARCHAR(10) NOT NULL,
 Thuong DECIMAL(10,2),
 LDThuong NVARCHAR(100),
 KhauTru DECIMAL(10,2),
 LDKhauTru NVARCHAR(100),
 FOREIGN KEY (MaNV) REFERENCES NHAN_VIEN(MaNV)
 )
 GO

 CREATE TABLE BANG_LUONG (
 MaBL VARCHAR(10) NOT NULL PRIMARY KEY,
 MaNV VARCHAR(10) NOT NULL,
 LuongCoBan DECIMAL(10,2),
 SoNgayCong INT,
 HeSoLuong DECIMAL(5,2),
 Thuong DECIMAL(10,2),
 KhauTru DECIMAL(10,2),
 TongLuong DECIMAL(10,2),
 FOREIGN KEY (MaNV) REFERENCES NHAN_VIEN(MaNV)
 )
 GO

